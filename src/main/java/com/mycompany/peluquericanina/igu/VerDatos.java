    private void cargarTabla() {
        //defino el modelo que quiero que tenga la tabla
        DefaultTableModel modeloTabla = new DefaultTableModel () {
            //hago que fila y columna no sean editables
            @Override
            public boolean isCellEditable (int ros, int column) {
                return false;
            }
        };
        
        //establezco los títulos de las columnas
        String titulos[] = {"Num", "Nomnre", "Color", "Raza", "Alérgico", "At.Esp.", "Dueño", "Cel."};
        modeloTabla.setColumnIdentifiers(titulos);
        
        //carga de datos desde la bb.dd.
        List<Mascota> listaMascotas = control.traerMascotas();
        
        //recorro la lista y muestro cada uno de los elementos en la tabla
        if (listaMascotas!=null) {
            for (Mascota masco : listaMascotas) {
                Object[] objeto = {masco.getNum_cliente(), masco.getNombre(), masco.getColor(),
                    masco.getRaza(), masco.getAlergico(), masco.getAtencion_especial(),
                    masco.getUnDuenio().getNombre(), masco.getUnDuenio().getCelDuenio()};
                
                modeloTabla.addRow(objeto);
            }
        }
        
        tblMascota.setModel(modeloTabla);
    }

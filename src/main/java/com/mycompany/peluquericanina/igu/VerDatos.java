    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //controlo que la tabla no está vacía
        if (tblMascota.getRowCount() > 0) {
            //controlo que se haya seleccionado un registro
            if (tblMascota.getSelectedRow()!=-1) {
                //obtengo la id de la mascota a eliminar
                int num_cliente = Integer.parseInt(String.valueOf(tblMascota.getValueAt(tblMascota.getSelectedRow(), 0)));
                //llamo al método borrer
                control.borrarMascota(num_cliente);
                //aviso al usuario que se borró correctamente
                mostrarMensaje("Mascota eliminada correctamente", "Info", "Borrado de mascota");
                cargarTabla();
            }
            else {
                mostrarMensaje("No seleccionó ninguna mascota", "Error", "Error al eliminar");
            }
        }
        else {
            mostrarMensaje("No hay nada para eliminar en la tabla", "Error", "Error al eliminar");
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        //controlo que la tabla no está vacía
        if (tblMascota.getRowCount() > 0) {
            //controlo que se haya seleccionado un registro
            if (tblMascota.getSelectedRow()!=-1) {
                //obtengo la id de la mascota a editar
                int num_cliente = Integer.parseInt(String.valueOf(tblMascota.getValueAt(tblMascota.getSelectedRow(), 0)));
                ModificarDatos pantallaModif = new ModificarDatos(num_cliente);
                pantallaModif.setVisible(true);
                pantallaModif.setLocationRelativeTo(null);
                
                this.dispose();
            }
            else {
                mostrarMensaje("No seleccionó ninguna mascota", "Error", "Error al eliminar");
            }
        }
        else {
            mostrarMensaje("No hay nada para eliminar en la tabla", "Error", "Error al eliminar");
        }
        
    }//GEN-LAST:event_btnEditarActionPerformed
    
    //método para mestrar mensaje de éxito o error
    public void mostrarMensaje (String mensaje, String tipo, String titulo) {
        JOptionPane optionPane = new JOptionPane(mensaje);
        if(tipo.equals("Info")) {
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        }
        else if (tipo.equals("Error")) {
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        
        JDialog dialog =  optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }


